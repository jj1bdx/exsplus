-export([all/0]).
-export([testloop_test/1]).
 
all() ->
    [testloop_test].

-spec testloop(pos_integer()) -> list().

testloop(N) ->
    R = exsplus:seed0(),
    testloop(N, R, []).

testloop(0, _, L) ->
    lists:reverse(L);
testloop(N, R, L) ->
    {V, R1} = exsplus:next(R),
    testloop(N - 1, R1, [V|L]).

testloop_test(_Config) ->
    Refval = test_refval(),
    Testval = testloop(length(Refval)),
    case Refval =:= Testval of
        true -> ok;
        Else -> exit(Else)
    end.

